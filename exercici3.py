numero1 = int(input("Numero 1: "))
numero2 = int(input("Numero 2: "))

if numero1 > numero2: #  Si el primer numero es major que el segon fara el print

    print(f"{numero1} és major que  {numero2}")
  
elif numero1  == numero2: ##  Si el primer numero es igual al segon fara el print

    print(f"Son iguals")

else : ##   Si el primer numero es menor que el segon fara el print

    print(f"{numero2} és mayor que {numero1}")
    
    